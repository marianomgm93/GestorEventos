/**
 * Define las categorías de eventos disponibles en el sistema.
 * Esta enumeración se utiliza para clasificar diferentes tipos de entretenimiento
 * o actividades que pueden ser programadas.
 */
package model;

public enum Categoria {

    /**
     * Eventos relacionados con la proyección de películas.
     */
    CINE,

    /**
     * Espectáculos musicales en vivo.
     */
    CONCIERTO,

    /**
     * Representaciones dramáticas o comedias escenificadas.
     */
    TEATRO,

    /**
     * Espectáculos unipersonales de comedia o monólogos.
     */
    STAND_UP,

    /**
     * Eventos deportivos, como partidos de fútbol, hockey, etc.
     */
    PARTIDO
}